export const getLanguageCode = (languageName) => {
  const languageMap = {
    'English': 'en',
    'Filipino': 'fil',
    'Spanish': 'es',
    'Chinese': 'zh',
    'Japanese': 'ja',
    'Korean': 'ko',
  }
  return languageMap[languageName] || 'en'
}

export const getTranslations = (language) => {
  const code = getLanguageCode(language)
  return translations[code] || translations.en
}

